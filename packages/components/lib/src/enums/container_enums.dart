/// 📏 **Container Size Enum**
///
/// Defines different size variants for containers.
/// Used to provide consistent sizing across the application.
///
/// ```dart
/// OsmeaContainer(
///   size: ContainerSize.medium,
///   child: Text('Medium Container'),
/// )
/// ```
enum ContainerSize {
  /// Tiny container size
  tiny,

  /// Extra small container size
  extraSmall,

  /// Small container size
  small,

  /// Medium container size
  medium,

  /// Large container size
  large,

  /// Extra large container size
  extraLarge,
}
